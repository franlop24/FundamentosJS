// Operadores Matemáticos
// + y -
// * y /
// % y ** Módulo y potencia
console.log(2 + 2);
console.log(2 - 2);
console.log(2 * 2);
console.log(2 / 2);
console.log(2 % 2);
console.log(2 ** 2);

// Operadores de asignación
let a = 10;
a += 5; //a = a + 5
a -= 5;
a *= 5;
a /= 5;
a %= 5;
a **= 5;
console.log(a);

// Operadores de Comparación
console.log(2 > 2);
console.log(2 < 2);
console.log(2 >= 2);
console.log(2 <= 2);
console.log(2 === 2);
console.log(2 == 2);
console.log(2 != 2);
console.log(2 !== 2);

// Operadores Lógicos
console.log(true && true); // and
console.log(true || false); // or
console.log(!true); // not